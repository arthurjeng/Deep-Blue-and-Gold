void Hash::Print(void) const
{
  for (ui index = 0; index < hashLen; index++) PrintHexByte(codebyte[index]);
}

si Hash::CompareHash(const Hash& hash0, const Hash& hash1)
{
  // Compare two hashes, like strcmp()

  si compare = 0;
  ui index = 0;

  while ((compare == 0) && (index < hashLen))
  {
    const ui8 cb0 = hash0.GetCodeByte(index), cb1 = hash1.GetCodeByte(index);

    if (cb0 == cb1) index++; else compare = (cb0 < cb1) ? -1 : 1;
  };
  return compare;
}

bool Hash::SameHash(const Hash& hash0, const Hash& hash1)
{
  return CompareHash(hash0, hash1) == 0;
}

